    class Solution {
        public int waysToMakeFair(int[] nums) {
            int s1 = 0, s2 = 0;
            for (int i = 0; i < nums.length; i++) {
                if (i % 2 == 0) {
                    s2 += nums[i];
                } else {
                    s1 += nums[i];
                }
            }

            int t1 = 0, t2 = 0;
            int v1 = 0, v2 = 0;
            int res = 0;

            for (int i = 0; i < nums.length; i++) {
                if (i % 2 == 0) {
                    v2 = t2 + s1 - t1;
                    v1 = t1 + s2 - t2 - nums[i];
                    t2 += nums[i];
                } else {
                    v1 = t1 + s2 - t2;
                    v2 = t2 + s1 - t1 - nums[i];
                    t1 += nums[i];
                }
                if (v1 == v2) {
                    res++;
                }
            }
            return res;
        }
    }

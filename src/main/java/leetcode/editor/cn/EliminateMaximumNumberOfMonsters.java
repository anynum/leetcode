class Solution {
    public int eliminateMaximum(int[] dist, int[] speed) {
        if (dist.length <= 1) {
            return dist.length;
        }
        int[] arrivalTime = new int[dist.length];
        for (int i = 0; i < dist.length; i++) {
            // è¿™é‡Œè¦å‘ä¸Šå–æ•´ï¼Œä½†æ˜¯ç”±äº / æœ¬æ¥å°±æ˜¯å‘ä¸‹å–æ•´çš„ï¼Œæ‰€ä»¥åˆ†å­å‡ä¸€åï¼Œå‘ä¸‹å–æ•´ï¼Œåœ¨åŠ ä¸€
            arrivalTime[i] = (int) Math.ceil((dist[i] - 1) / speed[i] + 1);
        }

        Arrays.sort(arrivalTime);
        int res = 0;
        for (res = 2; res <= dist.length; res++) {
            if (arrivalTime[res - 1] < res) {
                return res - 1;
            }
        }
        return dist.length;
    }
}

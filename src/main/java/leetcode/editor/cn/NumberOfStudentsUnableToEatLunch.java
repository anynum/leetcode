class Solution {

      public int countStudents(int[] students, int[] sandwiches) {
          int[] tmp = new int[2];
          for (int student : students) {
              tmp[student]++;
          }

          for (int i = 0; i < sandwiches.length; i++) {
              if (--tmp[sandwiches[i]] == -1) {
                  return sandwiches.length - i;
              }
          }
          return 0;
      }
}

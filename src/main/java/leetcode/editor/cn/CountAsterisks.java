  //leetcode submit region begin(Prohibit modification and deletion)
class Solution {
      public int countAsterisks(String s) {
          int length = s.length();
          boolean flag = true;
          int ans = 0;
          for (int i = 0; i < length; i++) {
              char c = s.charAt(i);
              if (flag && c == '*'){
                  ans++;
              }
              if (c == '|'){
                  flag = !flag;
              }
          }
          return ans;
      }
}

    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public int[] distributeCandies(int candies, int num_people) {
            int[] ans = new int[num_people];

            int i = 1;
            for (; i < candies; i++) {
                ans[(i - 1) % num_people] += i;
                candies -= i;
            }
            if (candies > 0) {
                ans[(i - 1) % num_people] += candies;
            }
            return ans;
        }
    }
